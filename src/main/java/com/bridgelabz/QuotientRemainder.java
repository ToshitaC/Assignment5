package com.bridgelabz;

public class QuotientRemainder {
    public static void main(String [] args){
        int a = 89;
        int b = 2;
        int quotient = a / b;
        int remainder = a % b;
        System.out.println("The Quotient is:" + quotient);
        System.out.println("The Remainder is:" + remainder);
    }
}
